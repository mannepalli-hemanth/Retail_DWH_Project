  (SELECT COUNT(*) FROM RAW.CUSTOMERS_RAW) AS RAW_COUNT,
  (SELECT COUNT(*) FROM STAGING.STG_CUSTOMERS) AS VALID_COUNT,
  (SELECT COUNT(*) FROM STAGING.ERROR_CUSTOMERS) AS ERR0R_COUNT,
  (SELECT COUNT(*) FROM STAGING.STG_CUSTOMERS)
  +
  (SELECT COUNT(*) FROM STAGING.ERROR_CUSTOMERS) AS FINAL_STG;

