    ORDER_ID,
    CUSTOMER_ID,
    ORDER_DATE,
    ORDER_STATUS,
    TOTAL_AMOUNT,
    STORE_ID,
    CREATED_DATE,
    UPDATED_DATE,
    CURRENT_TIMESTAMP() AS LOAD_DATE
FROM TEM_VALIDATED_ORDERS
WHERE RECORD_STATUS = 'VALID';
