CREATE OR REPLACE TABLE ODS.ODS_CUSTOMERS(
    CUSTOMER_ID NUMBER,
    FIRST_NAME VARCHAR(100),
    LAST_NAME VARCHAR(100),
    EMAIL VARCHAR(100),
    PHONE VARCHAR(30),
    GENDER VARCHAR(10),
    DATE_OF_BIRTH DATE,
    CITY VARCHAR(100),
    STATE VARCHAR(100),
    COUNTRY VARCHAR(100),
    CUSTOMER_STATUS VARCHAR(100),
    CREATED_DATE TIMESTAMP,
    UPDATED_DATE TIMESTAMP,
    CREATED_AT TIMESTAMP,
    UPDATED_AT TIMESTAMP
);