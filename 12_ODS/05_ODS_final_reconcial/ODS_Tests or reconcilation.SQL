SELECT
OT.ORDER_ID,
OT.ORDER_ITEM_ID
FROM ODS.ODS_ORDER_ITEMS AS OT
LEFT JOIN ODS.ODS_ORDERS AS O
ON OT.ORDER_ID = O.ORDER_ID
WHERE O.ORDER_ID IS NULL;

SELECT
COUNT(DISTINCT OT.ORDER_ID) 
FROM ODS.ODS_ORDER_ITEMS AS OT
LEFT JOIN ODS.ODS_ORDERS AS O
ON OT.ORDER_ID = O.ORDER_ID
WHERE O.ORDER_ID IS NULL;

SELECT
    ORDER_ID
FROM RETAIL_DWH.STAGING.ERROR_ORDERS
WHERE ORDER_ID IN (SELECT
    DISTINCT OT.ORDER_ID
    FROM ODS.ODS_ORDER_ITEMS AS OT
    LEFT JOIN ODS.ODS_ORDERS AS O
    ON OT.ORDER_ID = O.ORDER_ID
    WHERE O.ORDER_ID IS NULL)
GROUP BY ORDER_ID
