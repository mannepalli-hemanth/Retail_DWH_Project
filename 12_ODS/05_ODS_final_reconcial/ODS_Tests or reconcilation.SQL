COUNT(*) - COUNT(DISTINCT CUSTOMER_ID) AS DUPLICATE_CHECK
FROM ODS.ODS_CUSTOMERS

UNION ALL

SELECT 'PRODUCTS' ,
COUNT(*) - COUNT(DISTINCT PRODUCT_ID) AS DUPLICATE_CHECK
FROM ODS.ODS_PRODUCTS

UNION ALL

SELECT 'ORDERS' ,
COUNT(*) - COUNT(DISTINCT ORDER_ID) AS DUPLICATE_CHECK
FROM ODS.ODS_ORDERS

UNION ALL

SELECT 'ORDER_ITEMS' ,
COUNT(*) - COUNT(DISTINCT ORDER_ITEM_ID) AS DUPLICATE_CHECK
FROM ODS.ODS_ORDER_ITEMS

UNION ALL

SELECT 'PAYMENTS' ,
COUNT(*) - COUNT(DISTINCT PAYMENT_ID) AS DUPLICATE_CHECK
FROM ODS.ODS_PAYMENTS

UNION ALL

SELECT 'SHIPMENTS' ,
COUNT(*) - COUNT(DISTINCT SHIPMENT_ID) AS DUPLICATE_CHECK
FROM ODS.ODS_SHIPMENTS



/* WE HAVE 6 TABLES FROM THIS WE DESCIDED WHICH IS PARENT TABLE AND CHILD TABLE. AND FROM THOSE 6 TABLES, TABLE WHICH IS HAVING THE OTHER TABLES IDENTETIES IN THE TABLE THEN IT IS CALLED THE CHILD TABLE 

SO FOR EXAMPLE 

ORDERS TABLE IS HAVING CUSTOMER_ID
SO,

CUSTOMER TABLE = PARENT TABLE
ORDER TABLE = CHILD TABLE */

-------- ORDERS

SELECT 
    O.CUSTOMER_ID,
    O.ORDER_ID
FROM ODS.ODS_ORDERS AS O
LEFT JOIN ODS.ODS_CUSTOMERS AS C
ON O.CUSTOMER_ID = C.CUSTOMER_ID
WHERE C.CUSTOMER_ID IS NULL;

SELECT
COUNT(*) AS ORP,
COUNT(DISTINCT O.CUSTOMER_ID) AS MISSING
FROM ODS.ODS_ORDERS AS O
LEFT JOIN ODS.ODS_CUSTOMERS AS C
ON O.CUSTOMER_ID = C.CUSTOMER_ID
WHERE C.CUSTOMER_ID IS NULL;

SELECT
    CUSTOMER_ID,
    COUNT(*) AS RECORD_COUNT
FROM STAGING.ERROR_CUSTOMERS 
WHERE CUSTOMER_ID IN (
         SELECT 
            DISTINCT O.CUSTOMER_ID
            FROM ODS.ODS_ORDERS AS O
            LEFT JOIN ODS.ODS_CUSTOMERS AS C
            ON O.CUSTOMER_ID = C.CUSTOMER_ID
            WHERE C.CUSTOMER_ID IS NULL)
GROUP BY CUSTOMER_ID
ORDER BY RECORD_COUNT DESC;

