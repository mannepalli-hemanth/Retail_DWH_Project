---- ORDER_ITEMS

SELECT 
    OT.ORDER_ID,
    OT.PRODUCT_ID
FROM ODS.ODS_ORDER_ITEMS AS OT 
LEFT JOIN ODS.ODS_PRODUCTS AS OP 
ON OT.PRODUCT_ID = OP.PRODUCT_ID
WHERE OP.PRODUCT_ID IS NULL; 

SELECT
COUNT(*) AS CNT,
COUNT(DISTINCT OT.PRODUCT_ID) AS DIST
FROM ODS.ODS_ORDER_ITEMS AS OT 
LEFT JOIN ODS.ODS_PRODUCTS AS OP 
ON OT.PRODUCT_ID = OP.PRODUCT_ID
WHERE OP.PRODUCT_ID IS NULL;

SELECT
   PRODUCT_ID,
   COUNT(*) AS CNT
FROM STAGING.ERROR_PRODUCTS
WHERE PRODUCT_ID IN (
                    SELECT
                    DISTINCT OT.PRODUCT_ID
                    FROM ODS.ODS_ORDER_ITEMS AS OT
                    LEFT JOIN ODS.ODS_PRODUCTS AS OP
                    ON OT.PRODUCT_ID = OP.PRODUCT_ID
                    WHERE OP.PRODUCT_ID IS NULL
)
GROUP BY PRODUCT_ID
ORDER BY CNT DESC;

