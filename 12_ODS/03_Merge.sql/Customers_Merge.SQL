/*
MERGE INTO TARGET TABLE AS T 
USING SOURCE TABLE AS S
ON (RELATED COLUMNS)
WHEN MATCHED THEN
UPDATE SET
  "COLUMNS"
WHEN NOT MATCHED THEN
INSERT (
 TARGET COLUMNS
)
VALUES(
 SOURCE COLUMNS
);
*/

MERGE INTO ODS.ODS_CUSTOMERS AS T
USING RAW.TEMP_CUSTOMERS_INCREMENTAL AS S
ON T.CUSTOMER_ID = S.CUSTOMER_ID

-- WHEN MATCHED, WE USE UPDATE
WHEN MATCHED THEN
UPDATE SET
    T.FIRST_NAME = S.FIRST_NAME,
    T.LAST_NAME = S.LAST_NAME,
    T.EMAIL = S.EMAIL,
    T.PHONE = S.PHONE,
    T.GENDER = S.GENDER,
    T.DATE_OF_BIRTH = S.DATE_OF_BIRTH,
    T.CITY = S.CITY,
    T.STATE = S.STATE,
    T.COUNTRY = S.COUNTRY,
    T.CUSTOMER_STATUS = S.CUSTOMER_STATUS,
    T.CREATED_DATE = S.CREATED_DATE,
    T.UPDATED_DATE = S.UPDATED_DATE,
    T.UPDATED_AT = CURRENT_TIMESTAMP()

WHEN NOT MATCHED THEN 

INSERT 
(
    CUSTOMER_ID,
    FIRST_NAME,
    LAST_NAME,
    EMAIL,
    PHONE,
    GENDER,
    DATE_OF_BIRTH,
    CITY,
    STATE,
    COUNTRY,
    CUSTOMER_STATUS,
    CREATED_DATE,
    UPDATED_DATE,
    CREATED_AT,
    UPDATED_AT
)
VALUES
(
    S.CUSTOMER_ID,
    S.FIRST_NAME,
    S.LAST_NAME,
    S.EMAIL,
    S.PHONE,
    S.GENDER,
    S.DATE_OF_BIRTH,
    S.CITY,
    S.STATE,
    S.COUNTRY,
    S.CUSTOMER_STATUS,
    CREATED_DATE,
    UPDATED_DATE,
    CURRENT_TIMESTAMP(),
    CURRENT_TIMESTAMP()
);