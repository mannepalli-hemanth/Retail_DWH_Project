
--- VERIFY INCEREMENTAL RECORDS
SELECT
    CUSTOMER_ID,
    CUSTOMER_STATUS,
    CREATED_DATE,
    UPDATED_DATE,
    CREATED_AT,
    UPDATED_AT 
FROM ODS.ODS_CUSTOMERS
WHERE CUSTOMER_ID IN (100002,100003,100004,105002,105003)
ORDER BY CUSTOMER_ID;

SELECT COUNT(*) FROM ODS.ODS_CUSTOMERS;
-- DUPLICATE CHECK
SELECT
    CUSTOMER_ID,
    COUNT(*)
FROM ODS.ODS_CUSTOMERS
GROUP BY CUSTOMER_ID
HAVING COUNT(*) > 1;