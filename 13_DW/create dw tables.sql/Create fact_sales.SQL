CREATE OR REPLACE TABLE DW.FACT_SALES(
    SALES_KEY NUMBER AUTOINCREMENT PRIMARY KEY,
    ORDER_ID NUMBER,
    ORDER_ITEM_ID NUMBER,
    CUSTOMER_KEY NUMBER,
    PRODUCT_KEY NUMBER,
    ORDER_DATE_KEY NUMBER,
    QUANTITY NUMBER,
    UNIT_PRICE NUMBER(10,2),
    DISCOUNT NUMBER(10,2),
    GROSS_AMOUNT NUMBER(10,2),
    NET_SALES NUMBER(10,2),
    TOTAL_COST NUMBER(10,2),
    PROFIT NUMBER(10,2),
    CREATED_AT TIMESTAMP,
    UPDATED_AT TIMESTAMP
);
