CREATE OR REPLACE TABLE DW.FACT_SHIPMENTS(
    SHIPMENT_KEY NUMBER AUTOINCREMENT PRIMARY KEY,
    SHIPMENT_ID NUMBER,
    ORDER_ID NUMBER,
    CUSTOMER_KEY NUMBER,
    SHIPPED_DATE_KEY NUMBER,
    DELIVERY_DATE_KEY NUMBER,
    COURIER_NAME VARCHAR(100),
    TRACKING_NUMBER VARCHAR(100),
    SHIPMENT_STATUS VARCHAR(50),
    CREATED_AT TIMESTAMP,
    UPDATED_AT TIMESTAMP
);