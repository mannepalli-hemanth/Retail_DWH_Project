SELECT
  (SELECT COUNT(*) FROM STAGING.STG_CUSTOMERS) AS STG_CNT,
  (SELECT COUNT(*) FROM ODS.ODS_CUSTOMERS) AS ODS_CNT,
  (SELECT COUNT(*) FROM DW.DIM_CUSTOMER WHERE CUSTOMER_KEY <> 0) AS DW_VALID_VNT,
  (SELECT COUNT(*) FROM DW.DIM_CUSTOMER) AS TTL_DW_CNT,
  (SELECT COUNT(DISTINCT CUSTOMER_ID) FROM DW.DIM_CUSTOMER) AS DW_DIST_CNT;
