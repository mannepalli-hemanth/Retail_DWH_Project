INSERT INTO TEMP_PRODUCTS_INCREMENTAL
SELECT
    (SELECT MAX(PRODUCT_ID)
    FROM STAGING.STG_PRODUCTS)
    + ROW_NUMBER() OVER 
    (ORDER BY PRODUCT_ID)
     AS
    PRODUCT_ID,
    PRODUCT_NAME,
    CATEGORY,
    SUB_CATEGORY,
    BRAND,
    UNIT_PRICE,
    COST_PRICE,
    PRODUCT_STATUS,
    CURRENT_TIMESTAMP() AS CREATED_DATE,
    CURRENT_TIMESTAMP() AS UPDATED_DATE,
    LOAD_DATE
FROM STAGING.STG_PRODUCTS
ORDER BY PRODUCT_ID
LIMIT 2;
