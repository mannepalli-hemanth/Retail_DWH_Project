INSERT INTO TEMP_PRODUCTS_INCREMENTAL
SELECT
    PRODUCT_ID,
    PRODUCT_NAME,
    CATEGORY,
    SUB_CATEGORY,
    BRAND,
    UNIT_PRICE + 100 AS UNIT_PRICE,
    COST_PRICE,
    'INACTIVE' AS PRODUCT_STATUS,
    CREATED_DATE,
    CURRENT_TIMESTAMP() AS UPDATED_DATE,
    LOAD_DATE
FROM STAGING.STG_PRODUCTS
WHERE PRODUCT_ID IN (SELECT PRODUCT_ID
                      FROM STAGING.STG_PRODUCTS
                      ORDER BY PRODUCT_ID
                      LIMIT 2);

